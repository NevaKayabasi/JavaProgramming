package day04_Variables;

public class Circle {
    public static void main(String[] args) {
        int radius = 7;
        double PI = 3.14;

        double area = PI * radius * radius;
        double perimeter = 2 * PI * radius;

        System.out.println("area = cm2 " + area);
        System.out.println("perimeter = cm" + perimeter);


    }
}
