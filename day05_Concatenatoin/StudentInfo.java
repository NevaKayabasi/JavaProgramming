package day05_Concatenatoin;

public class StudentInfo {
    public static void main(String[] args) {

        String student_name = "Neva";
        int age = 36;
        String schoolName = "Lund Universty";
        char gender = 'F';
        String phone_number = "(123) 456 212 145";
        boolean fullTime = true;

        System.out.println("student_name = " + student_name);
        System.out.println("age = " + age);
        System.out.println("schoolName = " + schoolName);
        System.out.println("gender = " + gender);
        System.out.println("phone_number = " + phone_number);
        System.out.println("fullTime = " + fullTime);



    }
}
/*
1. student name (String)
    			2. age (int)
    			3. gender (char)
    			4. school name (String)
    			5. phone number (String) (###)-###-####
    			6. full time (boolean)

 */