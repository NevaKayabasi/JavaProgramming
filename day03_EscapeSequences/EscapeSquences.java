package day03_EscapeSequences;

public class EscapeSquences {
    public static void main(String[] args) {
        System.out.println("Java \nPyhton \nC#");

        System.out.println(".......................");

        System.out.println("Hello Cydeo! \nHow are you today? \nWhat class do we have today?");

        System.out.println("\tJava is cool programming language"); // it is to make a Tab button

        System.out.println("/ \\ ");  // to make a  single backslash we need to give two backward slashes

        System.out.println("My favorite tv show is \"beyaz show\""); // in order to print double quote


    }
}
/*
\n starts a new line
\t is paragraph space (tab)
\\ single back slash
 */
